print('Мой словарь: ', my_dict)
print('Значение по существующему ключу: ', my_dict.get('Andrew', 'Такое имя отсутствует'))
print('Значение по отсутствующему ключу: ', my_dict.get('Fedor', 'Такое имя отсутствует'))
my_dict.update({'Max': 2005, 'Alex': 2010})
print('Значение удаленной пары: ', my_dict.pop('Petr'))
print('Обновленный словарь', my_dict)
